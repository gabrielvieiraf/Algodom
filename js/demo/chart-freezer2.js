var last_index = 0
function updateAllGraphs() {
  const sensor_id = 1
  GetSensorData(USER_ID, sensor_id, last_index,
      function () {
  	const dataframe = JSON.parse(this.responseText)
  	//console.log(dataframe)
  	for (datarow of dataframe) {
  	    var date = new Date(datarow.timestamp * 1000)
  	    addData(TemperatureChart, date, datarow.internal_temperature)
  	    addData(EnergyChart, date, datarow.energy)
  	    addData(CostChart, date, datarow.cost)
  	    addData(VoltageChart, date, datarow.voltage)
  	    addData(CurrentChart, date, datarow.current)
  	    last_index += 1
  	}
      }
  )
}

$(document).ready(function () {

    // To periodically get sensor data:
    const interval_ms = 100000
    updateAllGraphs()
    setInterval(updateAllGraphs, interval_ms)

});
